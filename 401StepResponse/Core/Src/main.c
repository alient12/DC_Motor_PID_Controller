/* USER CODE BEGIN 4 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim)
{
	// velocity
	v = (x - x_old);
//	if (v < 0)
//	{
//		v += 360;
//	}
	v *= 100;
	x_old = x;
	if (auto_mode == 1 && control_x == 1)
	{
		// map encoder sensor to -180 to 180
		if (x > 180)
		{
			x = x - 360;
		}
		error_x = (x - setpoint_x) * 1000 / 180;
		// error_x = -error_x;
		sum_error_x += error_x;
		// Portion term
		controller_out = Kp * error_x; //420000 is 25% 1680000 for 6v in 24v
		// Integral term
		// WindUp reset
		if (sum_error_x * error_x < 0)
			sum_error_x = 0;
		controller_out += Ki * sum_error_x;
		// differential
		delta_x = error_x - error_x_old;
		error_x_old = error_x;
		controller_out += Kd * delta_x;
		devider = devider_x;
	}
	if (auto_mode == 1 && control_x == 0)
	{
		error_v = (v - setpoint_v) / 180;
		// error_v = -error_v;
		sum_error_v += error_v;
		// Portion term
		controller_out = Kp_v * error_v; //420000 is 25% 1680000 for 6v in 24v
		// Integral term
		// WindUp reset
		if (sum_error_v * error_v < 0)
			sum_error_v = 0;
		controller_out += Ki_v * sum_error_v;
		// differential
		delta_v = error_v - error_v_old;
		error_v_old = error_v;
		controller_out += Kd_v * delta_v;
		devider = devider_v;
	}

	// set output value
	if (controller_out > 0)
	{
		if (controller_out > 100 * devider)
			controller_out = 100 * devider;
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1, 0);
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_2, 1);
		TIM2->CCR1 = (3864 * controller_out / devider + 33600);
	}
	else if(controller_out < 0)
	{
		if (controller_out > 100 * devider)
			controller_out = 100 * devider;
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1, 1);
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_2, 0);
		controller_out = -controller_out;
		TIM2->CCR1 = (3864 * controller_out / devider + 33600);
	}
	else
	{
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1, 1);
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_2, 1);
		TIM2->CCR1 = 0;
	}

}
